package com.pluralsight.training;

public class IntegerMethod {

	public static void main(String[] args) {
		// 1 -First Project

		System.out.println("Hello World!");

		// 2 -Method with integer

		int myVar;
		myVar = 50;

		System.out.println(myVar);

		int anotherVar = 100;
		System.out.println(anotherVar);

		myVar = anotherVar;
		System.out.println(myVar);

		anotherVar = 250;
		System.out.println(anotherVar);

		System.out.println(++myVar);
		System.out.println(myVar);
		System.out.println(--myVar);
		System.out.println(myVar);
		System.out.println(myVar++);
		System.out.println(myVar);

	}

}
